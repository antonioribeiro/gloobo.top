                    <p>RIO DE JANEIRO - Nunca antes na história desse país tivemos um ano tão turbulento como 2016 - e olha que ele mal começou! Mas 2017 promete vir cheio de novidades reconfortantes, em especial na área de tecnologia. No próximo ano, o Google Fiber - o serviço de banda ultralarga criado pelo Google -  vai aterrizar no Brasil. O Google Fibra (como foi "aportuguesado") possui a incrível velocidade de 1Gb/s (1.000 megas!!!) sem limite de downloads. Para se ter uma ideia, a média de velocidade das operadoras do Brasil – Oi, TIM, NET, GVT, Vivo e outras – é de 2 Mbps. Ou seja: a velocidade será 500 vezes maior.</p>

                    <div class="foto">
                        <figure><img height="420" width="700" itemprop="image" src="img/google-fiber1.jpg">
                            <figcaption>
                                Google
                            </figcaption>
                        </figure>
                    </div>

                    <p>E para quem pensou que estávamos em último lugar em tudo, mais uma boa notícia: o Brasil é o único país, além dos EUA, que tem previsão de ter o serviço. Segundo o diretor-geral do Google no Brasil, Fíbrio Coelho - responsável pela escolha da logo do serviço: um fofo coelhinho multicolorido - a novidade é um avanço para a nação. "Do ponto de vista de políticas públicas, o Google Fibra está contribuindo para a construção de cidades inteligentes, conectadas e com alta transmissão de dados. Antes dele, somente centros de pesquisa na Finlândia fizeram testes com velocidade semelhante", comenta.</p>

                    <p>A notícia vem em um momento conturbado. Não estamos falando de impeachment, mas das novas limitações impostas pelas operadoras de internet. No novo modelo, que deve passar a valer também em 2017, você terá uma franquia de dados, ou seja, um limite de quanto você poderá navegar e fazer downloads na web. Ultrapassando essa cota, sua conexão poderá ter a velocidade reduzida ou até mesmo cortada até o fim do mês. Para continuar usando a internet normalmente, será necessário pagar uma taxa extra para aumentar a franquia.</p>

                    <div class="foto">
                        <figure><img height="420" width="700" itemprop="image" src="img/google-fiber-wall.jpg">
                            <figcaption>
                                Google
                            </figcaption>
                        </figure>
                    </div>

                    <p>É exatamente o mesmo sistema adotado nos pacotes de internet móvel. E as semelhanças não param por aí! Os sintomas gerados no usuário são bem parecidos aos de quando ele recebe aquele temido SMS dizendo que sua franquia de dados atingiu o limite e é necessário desembolsar uma bufunfa para continuar navegando: medo, confusão mental, pânico da mensagem no WhatsApp não completar, temor de ter de encarar as pessoas nos transportes públicos, de ter de cumprimentar quem pegou o mesmo elevador que você, ter de interagir na mesa de bar (sem a ajuda do Tinder), e, é claro, o mais temido de todos os temores, ter que de fato conversar com sua família durante o jantar.</p>

                    <p>Tudo isso poderia ser realidade, mas eis que surge o Google Fibra. A felicidade do sistema pode ser resumida no seguinte dado: enquanto no Google Fibra você pode baixar até 8.000 filmes por mês, na Vivo, por exemplo, sua franquia se esgotaria no 4º filme.</p>

                    <p>Além de fornecer banda ultralarga (isso mesmo! Não é só larga, é ultra!), o Fibra pretende mudar a maneira como você interage com a Internet através de um aplicativo. A iniciativa começa com o espaço de armazenamento na nuvem: 1 Terabyte.</p>

                    <div class="foto">
                        <figure><img height="420" width="700" itemprop="image" src="img/speed-test.jpg">
                            <figcaption>
                                Google
                            </figcaption>
                        </figure>
                    </div>

                    <p>Através da tecnologia DVR, você pode gravar até oito programas de televisão ao mesmo tempo, para assistir tudo depois. O Fibra também permite que você conecte a TV com o tablet Nexus 7 (o que é muito positivo, apesar de ninguém ter esse dispositivo no Brasil). Outros dispositivos, com sistemas operacionais iOS ou Android, também funcionam com o roteador desenvolvido especialmente pelo Google.</p>

                    <p>E se você também está ansioso para saber quanto custará essa maravilha, aí vai: o plano básico do Google Fibra será de R$ 129,90 por mês, quase o mesmo valor pago por míseros 30 megas na NET, por exemplo. Ah! E sabe o que é pior? Para baixar aqueles mesmos 8.000 sonhados filmes na Internet brasileira de 2017, você pagaria algo em torno de R$ 180 mil, 2.000 vezes o valor do Google Fibra! O pacote com TV e Internet em fibra, que sai por R$ 268.</p>

